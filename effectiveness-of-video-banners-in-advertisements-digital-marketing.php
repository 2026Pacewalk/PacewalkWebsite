                        <h5>Things need to keep in consideration:</h5>
                        <p><strong>Video file must be small</strong></p>
                        <p>Video length and resolution factor into the video file size should be taken in consideration.  It is always best to reduce the sizes of images, video and audio before producing your video because it will speed up production and you won’t need to compress the final video as much to reach the desired file size. Large video files take longer to load and can cause the video to pause mid-stream, depending on the visitor’s connection. One way to keep videos short is to choose content that loops seamlessly, which makes the video seem longer but takes up less space.</p>
                        <p><strong>Content with smooth transitions</strong></p>
                        <p>When you choose a video, pay attention to transitions to avoid quick cuts and excessive panning. Videos that appear to jump around catch the eye and can be jarring.</p>
                        <p><strong>Text Contrast</strong></p>
                        <p>Keep your background video compatible with your site’s styles, especially the color of any text you place over it. Ensure visitors can read your text at all points during the video.</p>
